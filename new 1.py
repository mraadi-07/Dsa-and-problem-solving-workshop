# what is linked list
# linked list is a data structure that stores data in nodes
# each node has a value and a pointer to the next node
# the first node is called the head
# the last node is called the tail

# what is a node
# a node is a data structure that stores data and a pointer to the next node

# what is a pointer
# a pointer is a data structure that stores the address of another data structure
# advantage of linked list is that it is easy to insert and delete nodes
